# Dictionaries -- Key-value pairs, it is unordered and mutable

my_dict = {"name": "Ankita", "age": 20, "city": "pune"}
print(my_dict)

# We can also use dict funct to create dictionary
# Inside dict function we pass argument with key and value

my_dict2 = dict(name="ankita", age=20, city="pune")
print(my_dict2)