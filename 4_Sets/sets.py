# Sets - it is unordered collection data type, it is mutable , no duplicates items

my_set = {"ankita", 2, True}
print(my_set)

# It doesn't allow the duplicate items so it skips the same or repeating element
my_set2 = {1,2,3,4,5,4,5,2}
print(my_set2)

# Creating set
my_set3 = set([1,2,3])
print(my_set3)

# as set is unordered collection it will not print the elements in ordered list
# So order of the element is not mandatory in set
my_set = set("hello")
print(my_set)
