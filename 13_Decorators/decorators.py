# Decorators - are a very powerful tool in python
# every advanced python programmer show know it

# Two decorators - 1 . Function decorator
#                  2 . Class Decorator


# Decorator is a function that takes another function as an argument 
# and extends the behaviour of this function without explicitly modifying it
# In other words it allows you to add new functionality to an existing function

# Decorator syntax
@mydecorator
def dosomething():
    pass
